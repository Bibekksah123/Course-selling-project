const mongoose=require("mongoose");

const adminSchema=mongoose.Schema({
    firstName: {
      type: String,
      required: true,
      trim: true,
    },
    lastName: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
  },

  { timestamps: true }
)

const adminModel=mongoose.model("Admin",adminSchema);
module.exports=adminModel